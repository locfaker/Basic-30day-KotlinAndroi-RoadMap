/**
 * ╔═══════════════════════════════════════════════════════════════╗
 * ║  NGUYÊN TẮC MVVM:                                             ║
 * ╠═══════════════════════════════════════════════════════════════╣
 * ║                                                               ║
 * ║  1. UNIDIRECTIONAL DATA FLOW (Luồng dữ liệu 1 chiều):         ║
 * ║     State đi xuống: ViewModel → UI                            ║
 * ║     Events đi lên: UI → ViewModel                             ║
 * ║                                                               ║
 * ║  2. SEPARATION OF CONCERNS:                                   ║
 * ║     UI: Chỉ hiển thị, không logic                             ║
 * ║     ViewModel: UI logic, không Android framework              ║
 * ║     Repository: Data logic                                    ║
 * ║                                                               ║
 * ║  3. SINGLE SOURCE OF TRUTH:                                   ║
 * ║     Database là nguồn chính                                   ║
 * ║     UI observe database                                       ║
 * ║                                                               ║
 * ║  4. REACTIVE:                                                 ║
 * ║     StateFlow/Flow để truyền data                             ║
 * ║     UI tự động update khi data thay đổi                       ║
 * ║                                                               ║
 * ╚═══════════════════════════════════════════════════════════════╝
 */
