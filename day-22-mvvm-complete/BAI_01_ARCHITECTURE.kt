 * ║                                                               ║
 * ║  ┌─────────────────────────────────────────────────────────┐  ║
 * ║  │                     UI LAYER                            │  ║
 * ║  │  ┌─────────────────────────────────────────────────┐    │  ║
 * ║  │  │  View (Composables)                             │    │  ║
 * ║  │  │  - Hiển thị UI                                  │    │  ║
 * ║  │  │  - Gửi events đến ViewModel                     │    │  ║
