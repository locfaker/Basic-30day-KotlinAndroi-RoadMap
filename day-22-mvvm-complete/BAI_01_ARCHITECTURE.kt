 * ║  │                          │                              │  ║
 * ║  │  ┌───────────────────────▼─────────────────────────┐    │  ║
 * ║  │  │  ViewModel                                      │    │  ║
 * ║  │  │  - Quản lý UI State                             │    │  ║
 * ║  │  │  - Xử lý UI logic                               │    │  ║
 * ║  │  │  - Không biết gì về UI cụ thể                   │    │  ║
 * ║  │  └───────────────────────┬─────────────────────────┘    │  ║
 * ║  └──────────────────────────┼──────────────────────────────┘  ║
 * ║                             │                                 ║
 * ║  ┌──────────────────────────▼──────────────────────────────┐  ║
 * ║  │                   DOMAIN LAYER (Optional)               │  ║
 * ║  │  Use Cases / Interactors                                │  ║
 * ║  │  - Business logic                                       │  ║
 * ║  └──────────────────────────┬──────────────────────────────┘  ║
 * ║                             │                                 ║
 * ║  ┌──────────────────────────▼──────────────────────────────┐  ║
 * ║  │                    DATA LAYER                           │  ║
 * ║  │  ┌─────────────────────────────────────────────────┐    │  ║
 * ║  │  │  Repository                                     │    │  ║
 * ║  │  │  - Single source of truth                       │    │  ║
